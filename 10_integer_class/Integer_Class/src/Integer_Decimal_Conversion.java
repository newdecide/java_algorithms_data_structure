public class Integer_Decimal_Conversion {
    public static void main(String[] args) {
        System.out.println("<10진수 진법 변환>");
        System.out.println("10진수 15 -> 2진수 : " + Integer.toBinaryString(15));
        System.out.println("10진수 10 -> 8진수 : " + Integer.toOctalString(10));
        System.out.println("10진수 26 -> 16진수 : " + Integer.toHexString(26));

    }
}
