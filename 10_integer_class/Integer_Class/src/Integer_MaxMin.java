public class Integer_MaxMin {
    public static void main(String[] args) {
        System.out.println("정수형 최댓값 : " + Integer.MAX_VALUE);
        System.out.println("정수형 최솟값 : " + Integer.MIN_VALUE);

        System.out.println("최댓값 메서드");
        System.out.println("125와 84중에 큰수 : " + Integer.max(125, 84));
        System.out.println("최솟값 메서드");
        System.out.println("125와 84중에 작은수 : " + Integer.min(125, 84));
    }
}
