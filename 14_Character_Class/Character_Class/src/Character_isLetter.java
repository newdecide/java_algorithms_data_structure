public class Character_isLetter {
    public static void main(String[] args) {
        System.out.println("문자 클래스 확인 메서드 (Character 타입이 맞으면 true, 틀리면 false)");
        System.out.println("c는 "+"문자가 맞나요? " + Character.isLetter('c'));
        System.out.println("1은 "+"문자가 맞나요? " + Character.isLetter(1));
    }
}
