public class Double_MaxMin {
    public static void main(String[] args) {
        System.out.println("실수형(64비트) 최댓값 : " + Double.MAX_VALUE);
        System.out.println("실수형(64비트) 최솟값 : " + Double.MIN_VALUE);
        System.out.println("실수형(64비트) 큰값 비교 메서드");
        System.out.println(Double.max(3450.1321323215656546446434, 2250.352345434355234));
        System.out.println("실수형(64비트) 작은 값 비교 메서드");
        System.out.println(Double.min(3450.1321323215656546446434, 2250.352345434355234));
    }
}
